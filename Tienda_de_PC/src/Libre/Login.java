    public void Iniciar()
    {
         con = new Conexion();
         Connection reg = con.getConnection();
         String AcoUsu ="0", ConUsu="0";
         ResultSet st;
         Statement cn;
        try
         {
         cn = reg.createStatement();
         st =cn.executeQuery("SELECT * FROM  `usuarios`  WHERE  `Usuario`='"+TxtUsuario.getText()+"' AND  `Clave`='"+TxtCon.getText()+"'");
         do {
              if(st.next()){
              AcoUsu = st.getString("Usuario");
              ConUsu = st.getString("Clave");
              
              if(AcoUsu.equals(TxtUsuario.getText()) && ConUsu.equals(TxtCon.getText()))
              {
                  this.dispose();
                  Usu=TxtUsuario.getText();
                  Con=TxtCon.getText();
                  Libre.Menu windows =new  Libre.Menu();
                  windows.setVisible(true);
                  windows.setLocationRelativeTo(null);
                  this.dispose();                 
            }
           }else
              {
                  JOptionPane.showMessageDialog(null, "Usuario o Contraseña inconrerta ","ERROR",JOptionPane.ERROR_MESSAGE,save);
                  Limpiar();
              }
         } while(st.next());
         }catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "Error"+ex );
         }
    }
    
