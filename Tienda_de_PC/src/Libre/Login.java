    public void Hora(){
    Calendar caledario = new GregorianCalendar();
    Date horaactual = new Date();
    caledario.setTime(horaactual);
    hora= caledario.get(Calendar.HOUR_OF_DAY)>9?""+caledario.get(Calendar.HOUR_OF_DAY):"0"+caledario.get(Calendar.HOUR_OF_DAY);
    minutos= caledario.get(Calendar.MINUTE)>9?""+caledario.get(Calendar.MINUTE):"0"+caledario.get(Calendar.MINUTE);
    segundos= caledario.get(Calendar.SECOND)>9?""+caledario.get(Calendar.SECOND):"0"+caledario.get(Calendar.SECOND);
    }
    
